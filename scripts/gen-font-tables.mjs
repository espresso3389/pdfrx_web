// Generates packages/viewer/src/font-tables.ts from the pdfrx example's
// google_fonts_resolver.dart so the Google Fonts hashes stay single-sourced.
//
// Usage: node scripts/gen-font-tables.mjs [path-to-pdfrx-repo]

import { readFileSync, writeFileSync } from 'node:fs';
import { dirname, join, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const repoRoot = resolve(dirname(fileURLToPath(import.meta.url)), '..');
const pdfrxRepo = resolve(process.argv[2] ?? process.env.PDFRX_REPO ?? join(repoRoot, '..', 'pdfrx'));
const dartFile = join(pdfrxRepo, 'packages', 'pdfrx', 'example', 'viewer', 'lib', 'google_fonts_resolver.dart');
const outFile = join(repoRoot, 'packages', 'viewer', 'src', 'font-tables.ts');

const source = readFileSync(dartFile, 'utf8');

// Parse: final _tableName = <int, _GoogleFontsFileSingle>{ ... };
const tableRe = /final (_[A-Za-z0-9]+) = <int, _GoogleFontsFileSingle>\{([\s\S]*?)\};/g;
const entryRe = /_GoogleFontsFileSingle\(\s*'([^']+)',\s*(\d+),\s*'([0-9a-f]{64})',\s*(\d+),?\s*\)/g;

const tables = {};
for (const tableMatch of source.matchAll(tableRe)) {
  const name = tableMatch[1];
  const entries = [];
  for (const e of tableMatch[2].matchAll(entryRe)) {
    entries.push({ faceName: e[1], weight: Number(e[2]), hash: e[3], length: Number(e[4]) });
  }
  if (entries.length > 0) tables[name] = entries;
}

const expectedTables = [
  '_arimo',
  '_arimoItalic',
  '_tinos',
  '_tinosItalic',
  '_cousine',
  '_cousineItalic',
  '_notoSans',
  '_notoSansItalic',
  '_notoSansSc',
  '_notoSansTc',
  '_notoSansJp',
  '_notoSansKr',
  '_notoSansThai',
  '_notoSansHebrew',
  '_notoSansArabic',
  '_notoSerif',
  '_notoSerifItalic',
  '_notoSerifSc',
  '_notoSerifTc',
  '_notoSerifJp',
  '_notoSerifKr',
  '_notoSerifThai',
  '_notoSerifHebrew',
  '_notoNaskhArabic',
];
const missing = expectedTables.filter((t) => !tables[t]);
if (missing.length > 0) {
  console.error(`Missing tables in ${dartFile}: ${missing.join(', ')}`);
  process.exit(1);
}

const tsName = (dart) => dart.substring(1); // _arimo -> arimo

let out = `/**
 * GENERATED FILE — do not edit.
 *
 * Generated by scripts/gen-font-tables.mjs from the pdfrx example's
 * google_fonts_resolver.dart. Re-run the script when the upstream tables
 * change. Each entry is a downloadable Google Fonts TTF identified by the
 * SHA-256 hash used by fonts.gstatic.com (https://fonts.gstatic.com/s/a/<hash>.ttf).
 */

export interface GoogleFontsFile {
  /** Compact Google Fonts family name (e.g. 'NotoSansJP'). */
  faceName: string;
  /** CSS font weight of this file. */
  weight: number;
  /** SHA-256 digest; also the fonts.gstatic.com file name. */
  hash: string;
  /** Expected byte length. */
  length: number;
}

export type WeightTable = Record<number, GoogleFontsFile>;

`;

for (const [dartName, entries] of Object.entries(tables)) {
  out += `export const ${tsName(dartName)}: WeightTable = {\n`;
  for (const e of entries) {
    out += `  ${e.weight}: { faceName: '${e.faceName}', weight: ${e.weight}, hash: '${e.hash}', length: ${e.length} },\n`;
  }
  out += `};\n\n`;
}

writeFileSync(outFile, out);
const total = Object.values(tables).reduce((n, t) => n + t.length, 0);
console.log(`Wrote ${outFile}: ${Object.keys(tables).length} tables, ${total} font files.`);
